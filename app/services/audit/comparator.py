def _member_key(rec: dict) -> tuple[str, str]:
    """Strict identity key: (DOB, last 4 of SSN)."""
    dob = rec.get("DOB") or rec.get("DOB__c") or ""
    # Salesforce sometimes returns dates as ISO datetime strings; normalize to date-only.
    dob_str = str(dob)[:10] if dob else ""
    return (
        dob_str,
        _ssn_last4(rec.get("socialSecurityNumber") or rec.get("SSN__c")),
    )


def _member_name_key(rec: dict) -> str:
    """Fallback key based on name — used when DOB/SSN are partial."""
    first = (rec.get("FirstName") or rec.get("First_Name__c") or "").strip().lower()
    last = (rec.get("LastName") or rec.get("Last_Name__c") or "").strip().lower()
    return f"{first} {last}".strip()


def _compare_members(ai_members: list[dict], sf_members: list[dict]) -> tuple[list[Finding], dict]:
    findings: list[Finding] = []

    # SF dedup: warn on duplicates within SF
    sf_keys: dict[tuple, list[dict]] = {}
    for m in sf_members:
        k = _member_key(m)
        sf_keys.setdefault(k, []).append(m)

    for k, group in sf_keys.items():
        if len(group) > 1 and k != ("", ""):
            name = f"{group[0].get('First_Name__c', '')} {group[0].get('Last_Name__c', '')}".strip()
            findings.append(Finding(
                category=DUPLICATE_MEMBER,
                severity="high",
                message=f"DUPLICATE MEMBER — {name} appears {len(group)} times in MuleSoft",
                detail={"key": list(k), "count": len(group)},
            ))

    ai_keys = {_member_key(m): m for m in ai_members if _member_key(m) != ("", "")}
    sf_keys_set = {k for k in sf_keys if k != ("", "")}

    matched = ai_keys.keys() & sf_keys_set
    ai_only_keys = ai_keys.keys() - sf_keys_set
    sf_only_keys = sf_keys_set - ai_keys.keys()

    # Name-based fuzzy fallback: if a member's strict (DOB, SSN) key
    # doesn't match across systems but their NAMES are similar (e.g.,
    # "Yvet Belen" vs "Yvett Belen" — single-letter OCR variant), treat
    # as matched and surface a low-severity mismatch finding instead of
    # flagging both directions as missing.
    ai_only_with_names = [
        (_member_name_key(ai_keys[k]), k) for k in ai_only_keys
    ]
    sf_only_with_names = [
        (_member_name_key(sf_keys[k][0]), k) for k in sf_only_keys
    ]

    fallback_matched_ai_keys: set[tuple[str, str]] = set()
    fallback_matched_sf_keys: set[tuple[str, str]] = set()
    fuzzy_match_pairs: list[tuple[str, str]] = []

    for ai_name, ai_k in ai_only_with_names:
        if not ai_name:
            continue
        best_ratio = 0.0
        best_sf_key: tuple[str, str] | None = None
        best_sf_name: str = ""
        for sf_name, sf_k in sf_only_with_names:
            if not sf_name or sf_k in fallback_matched_sf_keys:
                continue
            # 0.82 threshold catches single-letter OCR variants like
            # "Latisha" / "Latticia" (ratio 0.83) and "Yvet" / "Yvett"
            # (ratio 0.95). Tighter would miss those; looser risks
            # false-matching distinct names.
            ratio = _name_similarity(ai_name, sf_name)
            if ratio > best_ratio:
                best_ratio = ratio
                best_sf_key = sf_k
                best_sf_name = sf_name
        if best_ratio >= 0.82 and best_sf_key is not None:
            fallback_matched_ai_keys.add(ai_k)
            fallback_matched_sf_keys.add(best_sf_key)
            fuzzy_match_pairs.append((ai_name, best_sf_name))

    ai_only = ai_only_keys - fallback_matched_ai_keys
    sf_only = sf_only_keys - fallback_matched_sf_keys

    # Surface the fuzzy matches as a single low-severity finding per pair
    for ai_name, sf_name in fuzzy_match_pairs:
        if ai_name == sf_name:
            msg = (
                f"Member identity mismatch — '{ai_name.title()}' exists in "
                f"both systems but DOB/SSN keys differ"
            )
        else:
            msg = (
                f"Name spelling variant — AI: '{ai_name.title()}' vs "
                f"MuleSoft: '{sf_name.title()}' (likely same person)"
            )
        findings.append(Finding(
            category=VALUE_MISMATCH,
            severity="low",
            message=msg,
        ))

    for k in ai_only:
        m = ai_keys[k]
        name = f"{m.get('FirstName', '')} {m.get('LastName', '')}".strip()
        findings.append(Finding(
            category=MISSING_MEMBER,
            severity="high",
            message=(
                f"MuleSoft missing household member — {name} "
                f"(DOB: {m.get('DOB')}, SSN: ***-**-{k[1]})"
            ),
            detail={"name": name, "dob": m.get("DOB"), "ssn4": k[1]},
        ))

    for k in sf_only:
        m = sf_keys[k][0]
        name = f"{m.get('First_Name__c', '')} {m.get('Last_Name__c', '')}".strip()
        findings.append(Finding(
            category=IDP_MISSED_MEMBER,
            severity="medium",
            message=(
                f"AI may have missed household member — {name} "
                f"is in MuleSoft but not in AI extraction"
            ),
            detail={"name": name, "ssn4": k[1]},
        ))

    # Value mismatch on matched members (Disabled / Student)
    for k in matched:
        ai_m = ai_keys[k]
        sf_m = sf_keys[k][0]
        ai_dis = _norm(ai_m.get("disabled"))
        sf_dis = "y" if sf_m.get("Disabled__c") else "n" if sf_m.get("Disabled__c") is False else ""
        if ai_dis and sf_dis and ai_dis != sf_dis:
            findings.append(Finding(
                category=VALUE_MISMATCH,
                severity="medium",
                message=(
                    f"Disability mismatch — {ai_m.get('FirstName')} {ai_m.get('LastName')}: "
                    f"AI={ai_dis} MuleSoft={sf_dis}"
                ),
            ))

    return findings, {
        "agreements": len(matched),
        "disagreements": 0,
        "ai_only": len(ai_only),
        "sf_only": len(sf_only),
    }


