def _ssn_last4(ssn: str | None) -> str:
    if not ssn:
        return ""
    return str(ssn).replace("-", "").replace("*", "")[-4:]


def _norm(s: str | None) -> str:
    return (s or "").strip().lower()


def _money(v: Any) -> float | None:
    if v is None or v == "":
        return None
    try:
        return float(str(v).replace(",", "").replace("$", "").strip())
    except (ValueError, TypeError):
        return None


def _close(a: float, b: float, tolerance: float = 0.05) -> bool:
    if a == 0 and b == 0:
        return True
    if a == 0 or b == 0:
        return False
    return abs(a - b) / max(abs(a), abs(b)) < tolerance


# Policy thresholds for escalating a dollar-amount mismatch to CRITICAL.
# These are POLICY defaults, not values derived from any single sample — tune
# against real audit volume. Both gates must hold so a modest percentage on a
# big number AND a big percentage on a trivial number both stay REVIEW.
_MISMATCH_REL_GAP = 0.25       # relative gap (|a-b| / larger) at/above this, AND
_MISMATCH_ABS_GAP = 1000.0     # absolute gap (dollars) at/above this -> CRITICAL


def _value_mismatch_severity(
    a: float, b: float,
    *, rel_high: float = _MISMATCH_REL_GAP, abs_high: float = _MISMATCH_ABS_GAP,
) -> str:
    """Severity for a dollar-amount mismatch, escalated by magnitude.

    A large gap on a material amount is CRITICAL ("high"); everything else is
    "medium". e.g. $2,380 vs $55,884 (96%, $53k) -> high; $172 vs $272
    (37%, $99) -> medium. Thresholds are module-level policy (see above).
    """
    hi = max(abs(a), abs(b))
    if hi <= 0:
        return "medium"
    rel = abs(a - b) / hi
    if rel >= rel_high and abs(a - b) >= abs_high:
        return "high"
    return "medium"


# ---------------------------------------------------------------------------
# Synonym tables — map AI extraction names to MuleSoft's abbreviations.
# Both sides get normalized to a canonical form before comparison.
# Maps to either an explicit canonical or a longer matching string.
# ---------------------------------------------------------------------------

_INCOME_SOURCE_SYNONYMS: dict[str, str] = {
    # Social Security family
    "ssa": "social security administration",
    "ss": "social security administration",
    "social security": "social security administration",
    "social security administration": "social security administration",
    # SSI
    "ssi": "supplemental security income",
    "supplemental security income": "supplemental security income",
    # SSDI
    "ssdi": "social security disability",
    "social security disability": "social security disability",
    # State supplements (varies by state — group together)
    "ssp": "state supplement program",
    "state supplement program": "state supplement program",
    "massachusetts state supplement program": "state supplement program",
    "ma ssp": "state supplement program",
    "ma state supplement": "state supplement program",
    # Public assistance
    "tanf": "temporary assistance",
    "temporary assistance": "temporary assistance",
    "calworks": "temporary assistance",
    "colorado works program": "temporary assistance",
    "colorado works": "temporary assistance",
    "afdc": "temporary assistance",
    # Anticipated income — MuleSoft sometimes uses this as a generic
    # bucket; treat as "self-declared" so it can match AI's TIC extraction.
    "anticipated income": "self-declared",
    "self-declared": "self-declared",
    "self declared": "self-declared",
}


def _normalize_income_source(name: str | None) -> str:
    """Map an income source name to its canonical form for comparison."""
    raw = _norm(name)
    if not raw:
        return ""
    if raw in _INCOME_SOURCE_SYNONYMS:
        return _INCOME_SOURCE_SYNONYMS[raw]
    # Word-boundary fallback — handle "Massachusetts SSP", "MA SSI", etc.
    # A single-word synonym must match a WHOLE token (never an arbitrary
    # substring — otherwise 'ss' inside 'ssi' would wrongly map SSI → Social
    # Security). A multi-word synonym must appear as a contiguous phrase.
    # Longest synonyms first so the most specific one wins.
    raw_tokens = {t for t in re.split(r"[^a-z0-9]+", raw) if t}
    for synonym in sorted(_INCOME_SOURCE_SYNONYMS, key=len, reverse=True):
        syn_tokens = [t for t in re.split(r"[^a-z0-9]+", synonym) if t]
        if len(syn_tokens) == 1:
            if syn_tokens[0] in raw_tokens:
                return _INCOME_SOURCE_SYNONYMS[synonym]
        elif synonym in raw:
            return _INCOME_SOURCE_SYNONYMS[synonym]
    return raw


# Common organizational suffixes/prefixes — stripped before fuzzy matching
# so "Coherent Corp" matches "Coherent", "The Walt Disney Company" matches
# "Walt Disney Company", etc.
_ORG_SUFFIXES = {
    "corp", "corporation", "inc", "incorporated", "llc", "l.l.c",
    "ltd", "limited", "co", "company", "lp", "l.p", "llp",
    "plc", "pllc", "pc", "p.c", "group", "holdings",
}
_ORG_PREFIXES = {"the", "a", "an"}


def _normalize_org_name(name: str | None) -> str:
    """Strip common corporate suffixes/prefixes for fuzzy comparison.

    Examples:
      'Coherent Corp'             → 'coherent'
      'The Walt Disney Company'   → 'walt disney'
      'B&T Building Services Inc' → 'b&t building services'
      'XYZ, LLC.'                 → 'xyz'
    """
    raw = _norm(name)
    if not raw:
        return ""
    # Strip punctuation that commonly appears in business names.
    # Slash/ampersand/hyphen are token separators ("Aarp/New York Life" should
    # tokenize as ["aarp", "new", "york", "life"], not keep "aarp/new" joined),
    # so they become spaces. Apostrophes are removed (don't → dont, one token).
    cleaned = re.sub(r"[,./&\-]", " ", raw).replace("'", "")
    tokens = [t for t in cleaned.split() if t]
    # Drop trailing org suffixes
    while tokens and tokens[-1] in _ORG_SUFFIXES:
        tokens.pop()
    # Drop leading articles
    while tokens and tokens[0] in _ORG_PREFIXES:
        tokens.pop(0)
    return " ".join(tokens) if tokens else raw


def _name_similarity(a: str, b: str) -> float:
    """Best-of full-string and per-token similarity.

    Full-string ratio handles "Walt Disney Company" vs "The Walt Disney
    Company" cleanly. But OCR errors like "chilts" vs "chilis bar & grill"
    fail the full-string check (extra tokens drag the ratio down). Per-
    token best-pair similarity catches those.
    """
    if not a or not b:
        return 0.0
    full_ratio = SequenceMatcher(None, a, b).ratio()

    # Per-token best pair: split both, find the best matching pair.
    a_tokens = [t for t in a.split() if len(t) > 2]
    b_tokens = [t for t in b.split() if len(t) > 2]
    best_token = 0.0
    for ta in a_tokens:
        for tb in b_tokens:
            r = SequenceMatcher(None, ta, tb).ratio()
            if r > best_token:
                best_token = r

    return max(full_ratio, best_token)


def _normalize_account_type(t: str | None) -> str:
    """Strip variants of 'Account' suffix so 'Checking' matches 'Checking Account'.

    Also collapses some common synonyms:
      'investment account', 'stocks', 'brokerage' → 'investment'
      '401k', 'ira', 'retirement account'         → 'retirement'
    """
    raw = _norm(t)
    if not raw:
        return ""
    # Strip trailing " account" / " acct" / leading "personal "
    for suffix in (" account", " acct", " accts"):
        if raw.endswith(suffix):
            raw = raw[: -len(suffix)].strip()
    if raw.startswith("personal "):
        raw = raw[len("personal "):].strip()
    # Common synonyms
    synonyms = {
        "stocks": "investment",
        "brokerage": "investment",
        "investment account": "investment",
        "401k": "retirement",
        "ira": "retirement",
        "roth ira": "retirement",
        "money market": "savings",
        "cd": "savings",
        "certificate of deposit": "savings",
    }
    return synonyms.get(raw, raw)


def _account_types_compatible(a: str | None, b: str | None) -> bool:
    """Loose accountType match — allows 'Checking' ≈ 'Checking Account'."""
    na = _normalize_account_type(a)
    nb = _normalize_account_type(b)
    if not na or not nb:
        return True   # one side missing — don't block on type
    return na == nb


# ---------------------------------------------------------------------------
# Cluster helpers — collapse variant spellings into one finding so analysts
# don't see "AI missed walmart" + "AI missed wal-mart" + "AI missed walmart inc"
# as three findings when MuleSoft is just storing the same employer 3 ways.
# ---------------------------------------------------------------------------

def _cluster_income_keys(
    keys: set[tuple[str, str]],
    threshold: float = 0.85,
) -> list[list[tuple[str, str]]]:
    """Group (source, member) tuples whose source names are near-identical
    after org-name normalization. Same member required to cluster — different
    members keep their findings separate.
    Empty source names are dropped (they produce useless findings).
    """
    clusters: list[list[tuple[str, str]]] = []
    for k in keys:
        if not k[0]:
            continue
        norm = _normalize_org_name(k[0])
        attached = False
        for cluster in clusters:
            rep = cluster[0]
            if rep[1] != k[1]:
                continue
            if _name_similarity(norm, _normalize_org_name(rep[0])) >= threshold:
                cluster.append(k)
                attached = True
                break
        if not attached:
            clusters.append([k])
    return clusters


def _cluster_asset_keys(
    keys: set[tuple[str, str, str]],
    threshold: float = 0.85,
) -> list[list[tuple[str, str, str]]]:
    """Group asset keys that look like the same real-world account.

    Two keys cluster when accountTypes are compatible AND either:
      - both have a real (non-stub) last4 that matches, OR
      - source names fuzzy-match after org-name normalization.
    Keys with no source AND no real last4 are dropped.
    """
    clusters: list[list[tuple[str, str, str]]] = []
    for k in keys:
        src, atype, last4 = k
        is_stub = (not last4) or last4.startswith("~")
        if not src and is_stub:
            continue
        attached = False
        for cluster in clusters:
            rep_src, rep_type, rep_last4 = cluster[0]
            if not _account_types_compatible(atype, rep_type):
                continue
            rep_stub = (not rep_last4) or rep_last4.startswith("~")
            # Strong: matching real account numbers
            if not is_stub and not rep_stub and last4 == rep_last4:
                cluster.append(k)
                attached = True
                break
            # Fall back to source-name similarity
            if src and rep_src:
                if _name_similarity(
                    _normalize_org_name(src),
                    _normalize_org_name(rep_src),
                ) >= threshold:
                    cluster.append(k)
                    attached = True
                    break
        if not attached:
            clusters.append([k])
    return clusters


