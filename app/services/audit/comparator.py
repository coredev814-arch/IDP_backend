"""Compare IDP extraction against MuleSoft data from Salesforce.

Produces a list of structured Findings + a confidence score for the case.
Pure logic — no Salesforce calls, no I/O. Takes two dicts in, returns a result.
"""
import logging
from dataclasses import dataclass, field
from difflib import SequenceMatcher
from typing import Any

logger = logging.getLogger(__name__)


# Finding categories
DUPLICATE_MEMBER = "DUPLICATE_MEMBER"
DUPLICATE_INCOME = "DUPLICATE_INCOME"
DUPLICATE_ASSET = "DUPLICATE_ASSET"
MISSING_MEMBER = "MISSING_MEMBER"     # AI found, MuleSoft missed
MISSING_INCOME = "MISSING_INCOME"     # AI found, MuleSoft missed
MISSING_ASSET = "MISSING_ASSET"       # AI found, MuleSoft missed
IDP_MISSED_MEMBER = "IDP_MISSED_MEMBER"   # MuleSoft has, AI missed
IDP_MISSED_INCOME = "IDP_MISSED_INCOME"
IDP_MISSED_ASSET = "IDP_MISSED_ASSET"
VALUE_MISMATCH = "VALUE_MISMATCH"
COMPLIANCE = "COMPLIANCE"
# IDP-internal findings (not from comparison) — generated by the pipeline
# from cross-doc validation, special scenarios, bug detection, cert type
# rules, and field-level scoring.
INTERNAL_DISCREPANCY = "INTERNAL_DISCREPANCY"  # income/asset mismatches within the file
FIELD_QUALITY = "FIELD_QUALITY"                # [RED]/[YELLOW] field-level scoring findings
EXTRACTION_NOTE = "EXTRACTION_NOTE"            # workflow notes (pages excluded, name variants)


@dataclass
class Finding:
    category: str
    severity: str    # "high" | "medium" | "low" | "info"
    message: str
    detail: dict[str, Any] = field(default_factory=dict)


@dataclass
class Confidence:
    extraction_score: float
    agreement_rate: float
    case_confidence: float
    flag: str    # "green" | "yellow" | "red"
    agreements: int
    disagreements: int
    ai_only_records: int
    sf_only_records: int


@dataclass
class ComparisonResult:
    findings: list[Finding]
    confidence: Confidence


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _ssn_last4(ssn: str | None) -> str:
    if not ssn:
        return ""
    return str(ssn).replace("-", "").replace("*", "")[-4:]


def _norm(s: str | None) -> str:
    return (s or "").strip().lower()


def _money(v: Any) -> float | None:
    if v is None or v == "":
        return None
    try:
        return float(str(v).replace(",", "").replace("$", "").strip())
    except (ValueError, TypeError):
        return None


def _close(a: float, b: float, tolerance: float = 0.05) -> bool:
    if a == 0 and b == 0:
        return True
    if a == 0 or b == 0:
        return False
    return abs(a - b) / max(abs(a), abs(b)) < tolerance


# ---------------------------------------------------------------------------
# Synonym tables — map AI extraction names to MuleSoft's abbreviations.
# Both sides get normalized to a canonical form before comparison.
# Maps to either an explicit canonical or a longer matching string.
# ---------------------------------------------------------------------------

_INCOME_SOURCE_SYNONYMS: dict[str, str] = {
    # Social Security family
    "ssa": "social security administration",
    "ss": "social security administration",
    "social security": "social security administration",
    "social security administration": "social security administration",
    # SSI
    "ssi": "supplemental security income",
    "supplemental security income": "supplemental security income",
    # SSDI
    "ssdi": "social security disability",
    "social security disability": "social security disability",
    # State supplements (varies by state — group together)
    "ssp": "state supplement program",
    "state supplement program": "state supplement program",
    "massachusetts state supplement program": "state supplement program",
    "ma ssp": "state supplement program",
    "ma state supplement": "state supplement program",
    # Public assistance
    "tanf": "temporary assistance",
    "temporary assistance": "temporary assistance",
    "calworks": "temporary assistance",
    "colorado works program": "temporary assistance",
    "colorado works": "temporary assistance",
    "afdc": "temporary assistance",
    # Anticipated income — MuleSoft sometimes uses this as a generic
    # bucket; treat as "self-declared" so it can match AI's TIC extraction.
    "anticipated income": "self-declared",
    "self-declared": "self-declared",
    "self declared": "self-declared",
}


def _normalize_income_source(name: str | None) -> str:
    """Map an income source name to its canonical form for comparison."""
    raw = _norm(name)
    if not raw:
        return ""
    if raw in _INCOME_SOURCE_SYNONYMS:
        return _INCOME_SOURCE_SYNONYMS[raw]
    # Substring fallback — handle "Massachusetts SSP", "MA SSI", etc.
    for synonym, canonical in _INCOME_SOURCE_SYNONYMS.items():
        if synonym in raw or raw in synonym:
            return canonical
    return raw


# Common organizational suffixes/prefixes — stripped before fuzzy matching
# so "Coherent Corp" matches "Coherent", "The Walt Disney Company" matches
# "Walt Disney Company", etc.
_ORG_SUFFIXES = {
    "corp", "corporation", "inc", "incorporated", "llc", "l.l.c",
    "ltd", "limited", "co", "company", "lp", "l.p", "llp",
    "plc", "pllc", "pc", "p.c", "group", "holdings",
}
_ORG_PREFIXES = {"the", "a", "an"}


def _normalize_org_name(name: str | None) -> str:
    """Strip common corporate suffixes/prefixes for fuzzy comparison.

    Examples:
      'Coherent Corp'             → 'coherent'
      'The Walt Disney Company'   → 'walt disney'
      'B&T Building Services Inc' → 'b&t building services'
      'XYZ, LLC.'                 → 'xyz'
    """
    raw = _norm(name)
    if not raw:
        return ""
    # Strip punctuation that commonly appears in business names
    cleaned = raw.replace(",", " ").replace(".", " ").replace("'", "")
    tokens = [t for t in cleaned.split() if t]
    # Drop trailing org suffixes
    while tokens and tokens[-1] in _ORG_SUFFIXES:
        tokens.pop()
    # Drop leading articles
    while tokens and tokens[0] in _ORG_PREFIXES:
        tokens.pop(0)
    return " ".join(tokens) if tokens else raw


def _name_similarity(a: str, b: str) -> float:
    """Best-of full-string and per-token similarity.

    Full-string ratio handles "Walt Disney Company" vs "The Walt Disney
    Company" cleanly. But OCR errors like "chilts" vs "chilis bar & grill"
    fail the full-string check (extra tokens drag the ratio down). Per-
    token best-pair similarity catches those.
    """
    if not a or not b:
        return 0.0
    full_ratio = SequenceMatcher(None, a, b).ratio()

    # Per-token best pair: split both, find the best matching pair.
    a_tokens = [t for t in a.split() if len(t) > 2]
    b_tokens = [t for t in b.split() if len(t) > 2]
    best_token = 0.0
    for ta in a_tokens:
        for tb in b_tokens:
            r = SequenceMatcher(None, ta, tb).ratio()
            if r > best_token:
                best_token = r

    return max(full_ratio, best_token)


def _normalize_account_type(t: str | None) -> str:
    """Strip variants of 'Account' suffix so 'Checking' matches 'Checking Account'.

    Also collapses some common synonyms:
      'investment account', 'stocks', 'brokerage' → 'investment'
      '401k', 'ira', 'retirement account'         → 'retirement'
    """
    raw = _norm(t)
    if not raw:
        return ""
    # Strip trailing " account" / " acct" / leading "personal "
    for suffix in (" account", " acct", " accts"):
        if raw.endswith(suffix):
            raw = raw[: -len(suffix)].strip()
    if raw.startswith("personal "):
        raw = raw[len("personal "):].strip()
    # Common synonyms
    synonyms = {
        "stocks": "investment",
        "brokerage": "investment",
        "investment account": "investment",
        "401k": "retirement",
        "ira": "retirement",
        "roth ira": "retirement",
        "money market": "savings",
        "cd": "savings",
        "certificate of deposit": "savings",
    }
    return synonyms.get(raw, raw)


def _account_types_compatible(a: str | None, b: str | None) -> bool:
    """Loose accountType match — allows 'Checking' ≈ 'Checking Account'."""
    na = _normalize_account_type(a)
    nb = _normalize_account_type(b)
    if not na or not nb:
        return True   # one side missing — don't block on type
    return na == nb


