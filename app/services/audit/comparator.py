def _asset_key(rec: dict) -> tuple[str, str, str]:
    """sourceName + accountType + last 4 of accountNumber."""
    src = _norm(rec.get("sourceName") or rec.get("Source_Name__c"))
    acct_type = _norm(rec.get("accountType") or rec.get("Account_Type__c"))
    acct_num = str(rec.get("accountNumber") or "").strip()
    if not acct_num:
        # Fall back to a balance-based stub for SF records without account #
        bal = (rec.get("Cash_Value__c") or rec.get("VOA_Current__c") or
               rec.get("currentBalance") or "")
        acct_num = f"~{bal}"
    last4 = acct_num[-4:].lower() if len(acct_num) >= 4 else acct_num.lower()
    return (src, acct_type, last4)


def _compare_assets(
    ai_assets: list[dict], sf_assets: list[dict],
) -> tuple[list[Finding], dict]:
    findings: list[Finding] = []

    # Filter out SF asset records with no identifying fields — they're
    # placeholder rows MuleSoft sometimes creates that we can't compare
    # meaningfully. (Examples: real-estate stubs with no source/account.)
    sf_assets_filtered: list[dict] = []
    sf_orphan_count = 0
    for a in sf_assets:
        has_source = bool((a.get("Source_Name__c") or "").strip())
        has_account = bool((a.get("accountNumber") or "").strip())
        has_balance = _money(a.get("Cash_Value__c") or a.get("VOA_Current__c"))
        if not has_source and not has_account and not has_balance:
            sf_orphan_count += 1
            continue
        sf_assets_filtered.append(a)

    if sf_orphan_count > 0:
        logger.debug(
            "Filtered %d MuleSoft asset record(s) with no identifying fields",
            sf_orphan_count,
        )

    sf_keys: dict[tuple, list[dict]] = {}
    for a in sf_assets_filtered:
        k = _asset_key(a)
        sf_keys.setdefault(k, []).append(a)

    for k, group in sf_keys.items():
        if len(group) > 1:
            findings.append(Finding(
                category=DUPLICATE_ASSET,
                severity="high",
                message=(
                    f"DUPLICATE ASSET — '{group[0].get('Source_Name__c')}' "
                    f"({group[0].get('Account_Type__c')}) appears {len(group)} times"
                ),
            ))

    ai_keys = {_asset_key(a): a for a in ai_assets}
    matched = ai_keys.keys() & set(sf_keys.keys())
    ai_only_keys = ai_keys.keys() - set(sf_keys.keys())
    sf_only_keys = set(sf_keys.keys()) - ai_keys.keys()

    # Fuzzy-match remaining assets by sourceName similarity + same accountType
    # + close balance. Catches variants like:
    #   "Knights Of Columbus" (AI) vs "4633 KOC" (MuleSoft)
    #   "The Walt Disney Company" (AI) vs "Walt Disney Company" (MuleSoft)
    fuzzy_matched_ai_assets: set[tuple] = set()
    fuzzy_matched_sf_assets: set[tuple] = set()
    asset_fuzzy_pairs: list[tuple[tuple, tuple]] = []

    sf_only_list = list(sf_only_keys)
    for ai_k in ai_only_keys:
        ai_rec = ai_keys[ai_k]
        ai_src_norm = _normalize_org_name(ai_k[0])
        ai_type = ai_k[1]
        ai_bal = _money(ai_rec.get("currentBalance"))
        if not ai_src_norm and ai_bal is None:
            continue   # nothing to fuzzy-match on

        best_score = 0.0
        best_sf_k: tuple | None = None
        for sf_k in sf_only_list:
            if sf_k in fuzzy_matched_sf_assets:
                continue
            # accountType must be compatible after normalization
            # ('Checking' ≈ 'Checking Account', 'Investment' ≈ 'Brokerage').
            if not _account_types_compatible(ai_type, sf_k[1]):
                continue
            sf_rec = sf_keys[sf_k][0]
            sf_src_norm = _normalize_org_name(sf_k[0])
            sf_bal = _money(
                sf_rec.get("Cash_Value__c") or sf_rec.get("VOA_Current__c")
            )

            # Score = combination of name similarity and balance match.
            # Use _name_similarity (full-string + per-token) to catch
            # "Knights of Columbus" ↔ "4633 KOC" via balance, and
            # "Walt Disney" ↔ "The Walt Disney Company" via tokens.
            name_score = 0.0
            if ai_src_norm and sf_src_norm:
                name_score = _name_similarity(ai_src_norm, sf_src_norm)
            balance_match = (
                ai_bal is not None and sf_bal is not None
                and _close(ai_bal, sf_bal, tolerance=0.02)
            )

            # Near-perfect name match: same asset regardless of balance.
            # MuleSoft sometimes leaves Cash_Value__c as 0 or null even
            # when AI extracted a real balance from the PDF — surface as
            # a value mismatch rather than two missing/missed entries.
            if name_score >= 0.95:
                if name_score > best_score:
                    best_score = name_score
                    best_sf_k = sf_k
                    continue
            # Strong match: balance matches AND (name similar OR one source is empty)
            if balance_match and (name_score >= 0.85 or not ai_src_norm or not sf_src_norm):
                if name_score > best_score:
                    best_score = max(name_score, 0.86)
                    best_sf_k = sf_k
            # Moderate match: name very similar AND no balance contradiction
            elif name_score >= 0.85 and (ai_bal is None or sf_bal is None or balance_match):
                if name_score > best_score:
                    best_score = name_score
                    best_sf_k = sf_k

        if best_score >= 0.85 and best_sf_k is not None:
            fuzzy_matched_ai_assets.add(ai_k)
            fuzzy_matched_sf_assets.add(best_sf_k)
            asset_fuzzy_pairs.append((ai_k, best_sf_k))

    ai_only = ai_only_keys - fuzzy_matched_ai_assets
    sf_only = sf_only_keys - fuzzy_matched_sf_assets

    agreements = 0
    disagreements = 0

    for k in matched:
        ai_bal = _money(ai_keys[k].get("currentBalance"))
        sf_bal = _money(sf_keys[k][0].get("Cash_Value__c") or
                        sf_keys[k][0].get("VOA_Current__c"))
        if ai_bal is not None and sf_bal is not None:
            if _close(ai_bal, sf_bal, tolerance=0.02):
                agreements += 1
            else:
                disagreements += 1
                findings.append(Finding(
                    category=VALUE_MISMATCH,
                    severity="medium",
                    message=(
                        f"ASSET BALANCE MISMATCH — "
                        f"{sf_keys[k][0].get('Source_Name__c')} "
                        f"{sf_keys[k][0].get('Account_Type__c')}: "
                        f"AI ${ai_bal:,.2f} vs MuleSoft ${sf_bal:,.2f}"
                    ),
                ))
        else:
            agreements += 1

    # Surface fuzzy-matched assets as low-severity name variants.
    # Treat as agreements for confidence scoring. Also flag if the
    # balances differ between systems — that's a real discrepancy
    # the analyst should see.
    for ai_k, sf_k in asset_fuzzy_pairs:
        agreements += 1
        ai_rec = ai_keys[ai_k]
        sf_rec = sf_keys[sf_k][0]
        ai_src = ai_rec.get("sourceName") or "(unnamed)"
        sf_src = sf_rec.get("Source_Name__c") or "(unnamed)"
        ai_bal = _money(ai_rec.get("currentBalance"))
        sf_bal = _money(
            sf_rec.get("Cash_Value__c") or sf_rec.get("VOA_Current__c")
        )

        # Determine the right phrasing
        if ai_src.lower() != sf_src.lower():
            name_msg = (
                f"Asset name variant — AI: '{ai_src}' vs "
                f"MuleSoft: '{sf_src}' (likely same asset)"
            )
        else:
            name_msg = None  # only case differs — don't bother flagging

        balance_diff = (
            ai_bal is not None and sf_bal is not None
            and not _close(ai_bal, sf_bal, tolerance=0.02)
        )

        if balance_diff:
            findings.append(Finding(
                category=VALUE_MISMATCH,
                severity="medium",
                message=(
                    f"ASSET BALANCE MISMATCH — '{sf_src}' "
                    f"({sf_rec.get('Account_Type__c') or ai_rec.get('accountType')}): "
                    f"AI ${ai_bal:,.2f} vs MuleSoft ${sf_bal:,.2f}"
                ),
            ))
        elif name_msg:
            findings.append(Finding(
                category=VALUE_MISMATCH,
                severity="low",
                message=name_msg,
            ))

    for k in ai_only:
        rec = ai_keys[k]
        findings.append(Finding(
            category=MISSING_ASSET,
            severity="high",
            message=(
                f"MuleSoft missing asset — '{rec.get('sourceName')}' "
                f"({rec.get('accountType')}) ${_money(rec.get('currentBalance')) or 0:,.2f}"
            ),
        ))

    for k in sf_only:
        findings.append(Finding(
            category=IDP_MISSED_ASSET,
            severity="medium",
            message=(
                f"AI may have missed asset — '{sf_keys[k][0].get('Source_Name__c')}' "
                f"is in MuleSoft"
            ),
        ))

    return findings, {
        "agreements": agreements,
        "disagreements": disagreements,
        "ai_only": len(ai_only),
        "sf_only": len(sf_only),
    }


# ---------------------------------------------------------------------------
# Compliance findings (IDP-internal — no MuleSoft comparison needed)
# ---------------------------------------------------------------------------

def _classify_idp_finding(msg: str) -> tuple[str, str]:
    """Categorize and assign severity to a raw IDP finding string.

    The IDP pipeline produces strings in a few patterns:
      "[RED] X → field: Not extracted"          → field quality, high
      "[YELLOW] X → field: Not found ..."       → field quality, medium
      "Income discrepancy ... 92% difference"   → internal mismatch, medium
      "Income total mismatch: ..."              → internal mismatch, medium
      "... must be signed (Section 11)"         → compliance, high
      "Missing required compliance document"    → compliance, high
      "Pages 7: ... excluded"                   → extraction note, info
      "Name variants detected ..."              → extraction note, info
    Returns (category, severity).
    """
    lowered = msg.lower()

    if msg.startswith("[RED]"):
        return (FIELD_QUALITY, "high")
    if msg.startswith("[YELLOW]"):
        return (FIELD_QUALITY, "medium")

    if "discrepancy" in lowered or "mismatch" in lowered:
        return (INTERNAL_DISCREPANCY, "medium")

    if (
        "must be signed" in lowered
        or "missing required" in lowered
        or "resubmission required" in lowered
        or "certification requires" in lowered
        or "0 signed of" in lowered
    ):
        return (COMPLIANCE, "high")

    if "review" in lowered or "verify" in lowered:
        return (COMPLIANCE, "medium")

    if "excluded" in lowered or "name variants detected" in lowered:
        return (EXTRACTION_NOTE, "info")

    if "homeless" in lowered or "overpayment" in lowered:
        return (COMPLIANCE, "high")

    return (EXTRACTION_NOTE, "low")


def _idp_internal_findings(extraction: dict) -> list[Finding]:
    """Pull all findings produced by the IDP pipeline itself.

    Includes compliance, cross-doc validation, special scenarios, bug
    detection, cert type rules, and field-level scoring — basically
    everything the IDP pipeline writes into `extraction.findings`.

    These are NOT comparison findings (those come from _compare_*).
    """
    findings: list[Finding] = []
    seen_messages: set[str] = set()

    raw_findings = extraction.get("findings") or []
    for msg in raw_findings:
        if not msg or not isinstance(msg, str):
            continue
        msg_clean = msg.strip()
        if not msg_clean:
            continue
        # Dedup identical strings
        if msg_clean in seen_messages:
            continue
        seen_messages.add(msg_clean)

        category, severity = _classify_idp_finding(msg_clean)
        findings.append(Finding(
            category=category,
            severity=severity,
            message=msg_clean,
        ))

    # Belt-and-suspenders: also flag unsigned TIC if the IDP findings
    # list didn't include it but the cert_info says isSigned=No.
    cert = extraction.get("certification_info") or {}
    if str(cert.get("isSigned") or "").lower() == "no":
        unsigned_msg = "UNSIGNED TIC — TIC form not signed. Resubmission required."
        if unsigned_msg not in seen_messages and not any(
            "not signed" in f.message.lower() for f in findings
        ):
            findings.append(Finding(
                category=COMPLIANCE,
                severity="high",
                message=unsigned_msg,
            ))

    return findings


# ---------------------------------------------------------------------------
# Confidence score
# ---------------------------------------------------------------------------

def _calculate_confidence(
    extraction: dict,
    member_stats: dict, income_stats: dict, asset_stats: dict,
) -> Confidence:
    field_scores = extraction.get("field_scores") or {}
    extraction_score = float(field_scores.get("overall_composite") or 0.0)

    agreements = (member_stats["agreements"] + income_stats["agreements"]
                  + asset_stats["agreements"])
    disagreements = (member_stats["disagreements"] + income_stats["disagreements"]
                     + asset_stats["disagreements"])
    ai_only = (member_stats["ai_only"] + income_stats["ai_only"]
               + asset_stats["ai_only"])
    sf_only = (member_stats["sf_only"] + income_stats["sf_only"]
               + asset_stats["sf_only"])

    # Don't penalize "AI catches MuleSoft miss"; do penalize disagreements
    # and "AI missed something MuleSoft has".
    weight = agreements + disagreements + (sf_only * 0.5)
    if weight > 0:
        rate = (agreements - 0.5 * sf_only) / weight
        agreement_rate = max(0.0, min(1.0, rate))
    else:
        # No comparable records (MuleSoft has nothing) — confidence
        # rests on extraction quality alone.
        agreement_rate = 1.0 if ai_only > 0 else 0.5

    case_confidence = (extraction_score * 0.6) + (agreement_rate * 0.4)
    flag = ("green" if case_confidence >= 0.85
            else "yellow" if case_confidence >= 0.65
            else "red")

    return Confidence(
        extraction_score=extraction_score,
        agreement_rate=agreement_rate,
        case_confidence=case_confidence,
        flag=flag,
        agreements=agreements,
        disagreements=disagreements,
        ai_only_records=ai_only,
        sf_only_records=sf_only,
    )


