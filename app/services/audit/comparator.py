def _income_key(rec: dict) -> tuple[str, str]:
    """Source name (normalized through synonym table) + member name.

    AI extracts full names ('social security administration'), MuleSoft
    often uses abbreviations ('ssa'). Both go through the same synonym
    map so they match correctly.
    """
    source = rec.get("sourceName") or rec.get("Source_Name__c")
    member = rec.get("memberName") or rec.get("House_Member_Name__c") or ""
    # Normalize member: trim middle initial / 'r.' / 'jr' suffixes for fuzzy match.
    member_clean = _norm(member).replace(".", "").strip()
    member_tokens = [t for t in member_clean.split() if len(t) > 1]
    member_key = " ".join(member_tokens) if member_tokens else member_clean
    return (_normalize_income_source(source), member_key)


def _compare_income(
    ai_income: list[dict], ai_calculations: list[dict], sf_income: list[dict],
) -> tuple[list[Finding], dict]:
    findings: list[Finding] = []

    # Build map of AI annual amounts by source
    ai_annual: dict[tuple[str, str], float] = {}
    for calc in ai_calculations or []:
        k = (_norm(calc.get("sourceName")), _norm(calc.get("memberName")))
        amt = _money(calc.get("annualIncome"))
        if amt is not None:
            ai_annual[k] = amt

    # SF dedup check
    sf_keys: dict[tuple, list[dict]] = {}
    for inc in sf_income:
        k = _income_key(inc)
        sf_keys.setdefault(k, []).append(inc)

    # Detect SF duplicates within the same canonical source name
    for k, group in sf_keys.items():
        if len(group) > 1 and k[0]:
            findings.append(Finding(
                category=DUPLICATE_INCOME,
                severity="high",
                message=(
                    f"DUPLICATE INCOME — '{group[0].get('Source_Name__c')}' "
                    f"appears {len(group)} times in MuleSoft for the same member"
                ),
            ))

    ai_keys = set(_income_key(r) for r in ai_income)
    matched = ai_keys & set(sf_keys.keys())
    ai_only_keys = ai_keys - set(sf_keys.keys())
    sf_only_keys = set(sf_keys.keys()) - ai_keys

    # Fuzzy-match remaining AI-only and SF-only sources by name similarity.
    # Catches employer name variants like:
    #   "b&t building services" (AI) vs "b&t services inc." (SF)
    #   "ABC Staffing LLC" (AI)   vs "ABC Staffing"        (SF)
    # Same threshold (0.85) as member fuzzy matching.
    fuzzy_matched_ai: set[tuple[str, str]] = set()
    fuzzy_matched_sf: set[tuple[str, str]] = set()
    fuzzy_pairs: list[tuple[tuple[str, str], tuple[str, str]]] = []

    sf_only_list = list(sf_only_keys)
    for ai_k in ai_only_keys:
        if not ai_k[0]:    # skip empty source names
            continue
        ai_normalized = _normalize_org_name(ai_k[0])
        best_ratio = 0.0
        best_sf_k: tuple[str, str] | None = None
        for sf_k in sf_only_list:
            if sf_k in fuzzy_matched_sf or not sf_k[0]:
                continue
            # Match by source name similarity AND same member (or empty member).
            # If both have member names, they must also fuzzy-match.
            if ai_k[1] and sf_k[1]:
                member_ratio = SequenceMatcher(None, ai_k[1], sf_k[1]).ratio()
                if member_ratio < 0.7:
                    continue
            sf_normalized = _normalize_org_name(sf_k[0])
            # Strip corporate suffixes/prefixes AND check per-token
            # similarity — catches "Coherent Corp" ↔ "Coherent",
            # "The Walt Disney Company" ↔ "Walt Disney Company",
            # AND "chilts" ↔ "chilis bar & grill" (per-token: chilts/chilis).
            source_ratio = _name_similarity(ai_normalized, sf_normalized)
            if source_ratio > best_ratio:
                best_ratio = source_ratio
                best_sf_k = sf_k
        if best_ratio >= 0.85 and best_sf_k is not None:
            fuzzy_matched_ai.add(ai_k)
            fuzzy_matched_sf.add(best_sf_k)
            fuzzy_pairs.append((ai_k, best_sf_k))

    ai_only = ai_only_keys - fuzzy_matched_ai
    sf_only = sf_only_keys - fuzzy_matched_sf

    agreements = 0
    disagreements = 0

    for k in matched:
        ai_amt = ai_annual.get(k)
        sf_amt = _money(sf_keys[k][0].get("Gross_Member_Income__c"))
        if ai_amt is not None and sf_amt is not None and sf_amt > 0:
            if _close(ai_amt, sf_amt, tolerance=0.01):
                agreements += 1
            else:
                disagreements += 1
                findings.append(Finding(
                    category=VALUE_MISMATCH,
                    severity="medium",
                    message=(
                        f"INCOME MISMATCH — {sf_keys[k][0].get('Source_Name__c')}: "
                        f"AI ${ai_amt:,.2f} vs MuleSoft ${sf_amt:,.2f}"
                    ),
                    detail={
                        "source": sf_keys[k][0].get("Source_Name__c"),
                        "ai_amount": ai_amt,
                        "sf_amount": sf_amt,
                    },
                ))
        else:
            agreements += 1     # both present, can't compare values

    # Surface fuzzy-matched income sources as low-severity name variants.
    # Treat as agreements for confidence scoring (both systems have it).
    for ai_k, sf_k in fuzzy_pairs:
        agreements += 1
        findings.append(Finding(
            category=VALUE_MISMATCH,
            severity="low",
            message=(
                f"Income source name variant — AI: '{ai_k[0]}' vs "
                f"MuleSoft: '{sf_k[0]}' (likely same source)"
            ),
        ))

    for k in ai_only:
        findings.append(Finding(
            category=MISSING_INCOME,
            severity="high",
            message=f"MuleSoft missing income source — '{k[0]}' for {k[1] or 'household'}",
        ))

    for k in sf_only:
        findings.append(Finding(
            category=IDP_MISSED_INCOME,
            severity="medium",
            message=f"AI may have missed income source — '{k[0]}' is in MuleSoft",
        ))

    return findings, {
        "agreements": agreements,
        "disagreements": disagreements,
        "ai_only": len(ai_only),
        "sf_only": len(sf_only),
    }


