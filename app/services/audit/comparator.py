def _income_key(rec: dict) -> tuple[str, str]:
    """Source name (normalized through synonym table) + member name.

    AI extracts full names ('social security administration'), MuleSoft
    often uses abbreviations ('ssa'). Both go through the same synonym
    map so they match correctly.
    """
    source = rec.get("sourceName") or rec.get("Source_Name__c")
    member = rec.get("memberName") or rec.get("House_Member_Name__c") or ""
    # Normalize member: trim middle initial / 'r.' / 'jr' suffixes for fuzzy match.
    member_clean = _norm(member).replace(".", "").strip()
    member_tokens = [t for t in member_clean.split() if len(t) > 1]
    member_key = " ".join(member_tokens) if member_tokens else member_clean
    return (_normalize_income_source(source), member_key)


def _compare_income(
    ai_income: list[dict], ai_calculations: list[dict], sf_income: list[dict],
    *, ir_scoped: bool = False,
) -> tuple[list[Finding], dict]:
    findings: list[Finding] = []

    # Build map of AI annual amounts by source. Use _income_key so the
    # keys go through the same synonym normalization as ai_keys / sf_keys —
    # otherwise "SSA" (raw) won't find "social security administration"
    # (synonym-applied) when looking up a matched record.
    ai_annual: dict[tuple[str, str], float] = {}
    for calc in ai_calculations or []:
        amt = _money(calc.get("annualIncome"))
        if amt is not None:
            ai_annual[_income_key(calc)] = amt

    # SF dedup check
    sf_keys: dict[tuple, list[dict]] = {}
    for inc in sf_income:
        k = _income_key(inc)
        sf_keys.setdefault(k, []).append(inc)

    # Detect SF duplicates within the same canonical source name
    for k, group in sf_keys.items():
        if len(group) > 1 and k[0]:
            findings.append(Finding(
                category=DUPLICATE_INCOME,
                severity="high",
                message=(
                    f"DUPLICATE INCOME — '{group[0].get('Source_Name__c')}' "
                    f"appears {len(group)} times in MuleSoft for the same member"
                ),
            ))

    ai_keys = set(_income_key(r) for r in ai_income)
    matched = ai_keys & set(sf_keys.keys())
    ai_only_keys = ai_keys - set(sf_keys.keys())
    sf_only_keys = set(sf_keys.keys()) - ai_keys

    # Amount-based matching (member-agnostic). MuleSoft's member field is
    # unreliable — it sometimes holds a funding-program label ('LIHTC', 'HUD')
    # instead of a person — so a record can fail the (source, member) key match
    # even when source + annual amount clearly agree. Pair leftover records by
    # normalized source + close annual amount, ignoring member; the member
    # difference is surfaced as a low-severity note, not a false missing/missed.
    amount_pairs: list[tuple[tuple[str, str], tuple[str, str]]] = []
    amount_matched_ai: set[tuple[str, str]] = set()
    amount_matched_sf: set[tuple[str, str]] = set()
    for ai_k in ai_only_keys:
        if not ai_k[0]:
            continue
        ai_amt = ai_annual.get(ai_k)
        if ai_amt is None or ai_amt <= 0:
            continue
        for sf_k in sf_only_keys:
            if sf_k in amount_matched_sf or sf_k[0] != ai_k[0]:
                continue
            sf_amt = _money(sf_keys[sf_k][0].get("Gross_Member_Income__c"))
            # Same tolerance the exact-match income comparison uses (0.01),
            # not the asset tolerance — keep one income-amount tolerance.
            if sf_amt is not None and sf_amt > 0 and _close(ai_amt, sf_amt, tolerance=0.01):
                amount_matched_ai.add(ai_k)
                amount_matched_sf.add(sf_k)
                amount_pairs.append((ai_k, sf_k))
                break

    ai_only_keys = ai_only_keys - amount_matched_ai
    sf_only_keys = sf_only_keys - amount_matched_sf

    # Fuzzy-match remaining AI-only and SF-only sources by name similarity.
    # Catches employer name variants like:
    #   "b&t building services" (AI) vs "b&t services inc." (SF)
    #   "ABC Staffing LLC" (AI)   vs "ABC Staffing"        (SF)
    # Same threshold (0.85) as member fuzzy matching.
    fuzzy_matched_ai: set[tuple[str, str]] = set()
    fuzzy_matched_sf: set[tuple[str, str]] = set()
    fuzzy_pairs: list[tuple[tuple[str, str], tuple[str, str]]] = []

    sf_only_list = list(sf_only_keys)
    for ai_k in ai_only_keys:
        if not ai_k[0]:    # skip empty source names
            continue
        ai_normalized = _normalize_org_name(ai_k[0])
        best_ratio = 0.0
        best_sf_k: tuple[str, str] | None = None
        for sf_k in sf_only_list:
            if sf_k in fuzzy_matched_sf or not sf_k[0]:
                continue
            # Match by source name similarity AND same member (or empty member).
            # If both have member names, they must also fuzzy-match.
            if ai_k[1] and sf_k[1]:
                member_ratio = SequenceMatcher(None, ai_k[1], sf_k[1]).ratio()
                if member_ratio < 0.7:
                    continue
            sf_normalized = _normalize_org_name(sf_k[0])
            # Strip corporate suffixes/prefixes AND check per-token
            # similarity — catches "Coherent Corp" ↔ "Coherent",
            # "The Walt Disney Company" ↔ "Walt Disney Company",
            # AND "chilts" ↔ "chilis bar & grill" (per-token: chilts/chilis).
            source_ratio = _name_similarity(ai_normalized, sf_normalized)
            if source_ratio > best_ratio:
                best_ratio = source_ratio
                best_sf_k = sf_k
        if best_ratio >= 0.85 and best_sf_k is not None:
            fuzzy_matched_ai.add(ai_k)
            fuzzy_matched_sf.add(best_sf_k)
            fuzzy_pairs.append((ai_k, best_sf_k))

    ai_only = ai_only_keys - fuzzy_matched_ai
    sf_only = sf_only_keys - fuzzy_matched_sf

    agreements = 0
    disagreements = 0

    for k in matched:
        ai_amt = ai_annual.get(k)
        sf_amt = _money(sf_keys[k][0].get("Gross_Member_Income__c"))
        if ai_amt is not None and sf_amt is not None and sf_amt > 0:
            if _close(ai_amt, sf_amt, tolerance=0.01):
                agreements += 1
            else:
                disagreements += 1
                findings.append(Finding(
                    category=VALUE_MISMATCH,
                    severity=_value_mismatch_severity(ai_amt, sf_amt),
                    message=(
                        f"INCOME MISMATCH — {sf_keys[k][0].get('Source_Name__c')}: "
                        f"AI ${ai_amt:,.2f} vs MuleSoft ${sf_amt:,.2f}"
                    ),
                    detail={
                        "source": sf_keys[k][0].get("Source_Name__c"),
                        "ai_amount": ai_amt,
                        "sf_amount": sf_amt,
                    },
                ))
        else:
            agreements += 1     # both present, can't compare values

    # Surface fuzzy-matched income sources. If the dollar amounts also
    # differ, escalate to INCOME MISMATCH (medium) — name variants must
    # NOT hide real discrepancies. Otherwise emit the low-severity name
    # variant note. Mirrors the asset-side pattern.
    for ai_k, sf_k in fuzzy_pairs:
        sf_rec = sf_keys[sf_k][0]
        ai_amt = ai_annual.get(ai_k)
        sf_amt = _money(sf_rec.get("Gross_Member_Income__c"))
        amount_diff = (
            ai_amt is not None and sf_amt is not None and sf_amt > 0
            and not _close(ai_amt, sf_amt, tolerance=0.01)
        )

        if amount_diff:
            disagreements += 1
            findings.append(Finding(
                category=VALUE_MISMATCH,
                severity=_value_mismatch_severity(ai_amt, sf_amt),
                message=(
                    f"INCOME MISMATCH — '{sf_rec.get('Source_Name__c')}' "
                    f"(matched to AI '{ai_k[0]}'): "
                    f"AI ${ai_amt:,.2f} vs MuleSoft ${sf_amt:,.2f}"
                ),
                detail={
                    "source": sf_rec.get("Source_Name__c"),
                    "ai_source": ai_k[0],
                    "ai_amount": ai_amt,
                    "sf_amount": sf_amt,
                },
            ))
        else:
            agreements += 1
            findings.append(Finding(
                category=VALUE_MISMATCH,
                severity="low",
                message=(
                    f"Income source name variant — AI: '{ai_k[0]}' vs "
                    f"MuleSoft: '{sf_k[0]}' (likely same source)"
                ),
            ))

    # Amount-matched pairs are agreements by construction (same source, close
    # annual amount). Surface the member difference so the analyst can see when
    # MuleSoft's member field is wrong (e.g. 'LIHTC'/'HUD' instead of a person).
    for ai_k, sf_k in amount_pairs:
        agreements += 1
        ai_member = ai_k[1]
        sf_member = sf_k[1]
        if ai_member and sf_member and ai_member != sf_member:
            findings.append(Finding(
                category=VALUE_MISMATCH,
                severity="low",
                message=(
                    f"Income member differs — '{ai_k[0]}': AI member "
                    f"'{ai_member}' vs MuleSoft '{sf_member}' "
                    f"(matched by source + amount)"
                ),
            ))

    # AI-only — drop empty source names (useless finding "MuleSoft missing ''")
    ai_only_clean = {k for k in ai_only if k[0]}
    for k in ai_only_clean:
        findings.append(Finding(
            category=MISSING_INCOME,
            severity="high",
            message=f"MuleSoft missing income source — '{k[0]}' for {k[1] or 'household'}",
        ))

    # SF-only — cluster variant spellings ('walmart' / 'wal-mart' / 'walmart inc')
    # so MuleSoft's same-source-many-rows shows as one finding, not many.
    sf_only_clusters = _cluster_income_keys(sf_only)
    for cluster in sf_only_clusters:
        rep = cluster[0]
        member = rep[1] or "household"
        if ir_scoped:
            sev = "info"
            msg = (
                f"Income source '{rep[0]}' for {member} is in MuleSoft's "
                f"full-case record, not the interim packet — verify if changed (IR)"
            )
        elif len(cluster) > 1:
            sev = "medium"
            variants = ", ".join(f"'{k[0]}'" for k in cluster[:3])
            if len(cluster) > 3:
                variants += f", +{len(cluster) - 3} more"
            msg = (
                f"AI may have missed income source — '{rep[0]}' for {member} "
                f"(MuleSoft has {len(cluster)} variant spellings: {variants})"
            )
        else:
            sev = "medium"
            msg = f"AI may have missed income source — '{rep[0]}' for {member}"
        findings.append(Finding(
            category=IDP_MISSED_INCOME,
            severity=sev,
            message=msg,
        ))

    return findings, {
        "agreements": agreements,
        "disagreements": disagreements,
        "ai_only": len(ai_only_clean),
        "sf_only": len(sf_only_clusters),
    }


